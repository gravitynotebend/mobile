/**
 * Захардкоженный сценарий ответов AI-ассистента (демо/MVP).
 * Ассистент реагирует на свободный текст бухгалтера: распознаёт намерение по
 * ключевым словам и возвращает «живой» ответ — с проверками, ссылками на
 * конкретные документы и страницы, вариантами на выбор и подтверждениями.
 *
 * Это витрина возможностей: настоящая интеграция с LLM подключается позже,
 * интерфейс ответов (блоки) остаётся тем же.
 */

/** Ссылка на конкретный документ и страницу — открывается в просмотрщике по клику. */
export type DocRef = {
  name: string;
  ext: string;
  /** Открыть просмотрщик сразу на этой странице. */
  page?: number;
  /** Где именно смотреть — «стр. 1 · строка 14». */
  hint?: string;
};

/** Кликабельная подсказка: подставляет текст запроса от лица пользователя. */
export type Suggestion = { label: string; query: string };

export type DiffRow = { label: string; before: string; after: string };

export type ChecklistItem = {
  text: string;
  tone: "ok" | "warn" | "info";
  ref?: DocRef;
};

/** Блок содержимого ответа ассистента. */
export type Block =
  | { kind: "text"; text: string }
  | { kind: "refs"; title?: string; items: DocRef[] }
  | { kind: "checklist"; title?: string; items: ChecklistItem[] }
  | { kind: "diff"; title: string; rows: DiffRow[] }
  | { kind: "letter"; to: string; subject: string; body: string }
  | { kind: "options"; prompt?: string; items: Suggestion[] }
  | { kind: "confirm"; label: string; query: string; note?: string }
  | { kind: "success"; title: string; text?: string }
  | { kind: "followups"; items: Suggestion[] };

export type AssistantTurn = {
  /** Этапы «обдумывания», которые показываются до вывода ответа. */
  stages?: string[];
  /** Какое «настроение» показывает аватар во время обработки: поиск (лупа) или размышление. */
  mood?: "search" | "think";
  blocks: Block[];
};

const norm = (s: string) =>
  s
    .toLowerCase()
    .replace(/ё/g, "е")
    .replace(/[^a-zа-я0-9\s№]/gi, " ")
    .replace(/\s+/g, " ")
    .trim();

const hasAny = (t: string, ...words: string[]) => words.some((w) => t.includes(w));

